package com.modernjava.chapter9;

public class Point {
  private int x;
  private int y;

  public Point(int x, int y) {
    this.x = x;
    this.y = y;
  }

  public int getX() {
    return x;
  }

  public void setX() {
    this.x = x;
  }

  public int getY() {
    return y;
  }

  private void setY() {
    this.y = y;
  }
}
